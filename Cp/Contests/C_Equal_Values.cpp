#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define mod 1000000007
void solve()
{
    ll n;
    cin >> n;
    vector<ll> l(n);
    for(auto& i: l){
        cin >> i;
    }
    ll b=0, j=0;
    ll ans = (n-1)*l[0];
    while(b<n){
        while(l[j]==l[b]){
            j++;
        }
        ans = min(ans, (b*l[b]+(n-j)*l[b]));
        b = j;
    }
    cout << ans << endl;
}
